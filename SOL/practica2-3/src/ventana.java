    private void botonAñadirTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAñadirTodosActionPerformed
        // TODO add your handling code here:
        String primero[] = {"fol", "sistemas", "bases de datos", "programacion", "entornos", "marcas"};
        String segundo[] = {"datos", "interfaces", "servicios y procesos", "sistemas", "empresa", "dispositivos mòviles"};
        
        if (comboboxGrado.getSelectedItem().toString().equalsIgnoreCase("primero")) {
            if (combobox.getItemCount() < 6) {
                for (int i = 0; i < primero.length; i++) {

                    combobox.addItem(primero[i]);
                }

            } else {
                JOptionPane.showMessageDialog(this, "No se puede volver añadir los mismo cursos", "Error", JOptionPane.ERROR_MESSAGE);

            }
        }
        
        if (comboboxGrado.getSelectedItem().toString().equalsIgnoreCase("segundo")) {
            if (combobox.getItemCount() < 6) {
                for (int i = 0; i < segundo.length; i++) {

                    combobox.addItem(segundo[i]);
                }

            } else {
                JOptionPane.showMessageDialog(this, "No se puede volver añadir los mismo cursos", "Error", JOptionPane.ERROR_MESSAGE);

            }
        }
        
    
        
        
        
    }//GEN-LAST:event_botonAñadirTodosActionPerformed

