    private void botonAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAñadirActionPerformed
        // TODO add your handling code here:
        String nuevoElemento = txtNuevoElemento.getText(); 
        
        if(nuevoElemento.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "No puedes añadir un campo vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean existe = false;
        for (int i = 0; i < combobox.getItemCount(); i++) {
            if (combobox.getItemAt(i).equalsIgnoreCase(nuevoElemento)) {
                existe = true;
                break;

            }
        }

        if (existe) {
            
            JOptionPane.showMessageDialog(this, "El elemento ya existe en el ComboBox.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
          
            combobox.addItem(nuevoElemento);
           
        }

        // Limpiar el campo de texto después de añadir
        txtNuevoElemento.setText("");
    }//GEN-LAST:event_botonAñadirActionPerformed

